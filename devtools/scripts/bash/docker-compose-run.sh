#!/bin/bash
# ===================================================================
# Docker Compose起動スクリプト
# ===================================================================
# このスクリプトはDocker Composeサービスをビルドして起動します。
#
# 使用方法：
#   ./docker-compose-run.sh
#
# 機能：
# - 既存のイメージを再ビルド
# - バックグラウンドでサービス起動
# ===================================================================

echo "Building and starting services..."
docker compose up --build
